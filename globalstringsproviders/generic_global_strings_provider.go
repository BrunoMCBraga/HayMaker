var optionsMenu string = `	
	
Haymaker Cluster:
sh: Spinup Haymaker (i.e. create network, kubernetes cluster and nodes)
dh: Destroy Haymaker 

Kubernetes Orchestration:
gk: Generate Kubeconfig file and save it to file
sr: Spinup Docker repository
dr: Destroy Docker repository

sc: Spinup container and create service to expose them
dc: Destroy running container and associated service

pi: Build and push Docker image to registry
di: Delete Docker image from registry
dli: Delete local Docker image

fs: Full Haymaker start (i.e. create Kubernetes cluster, container registry, create Docker image, push it and deploy it)
fd: Full Haymaker destroy

Others:
is: Print information about service (e.g. external domain/IP)`

